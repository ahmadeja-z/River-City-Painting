class Customer {
  final String name;
  final String email;
  final String date;
  final String phoneNumber;
  final String source;
  final String addedBy;
  final String branch;
  final String address;

  Customer({
    required this.name,
    required this.email,
    required this.date,
    required this.phoneNumber,
    required this.source,
    required this.addedBy,
    required this.branch,
    required this.address,
  });
}
